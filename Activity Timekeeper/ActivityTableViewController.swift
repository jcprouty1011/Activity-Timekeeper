    // MARK: - Navigation

    // In a storyboard-based application, you will often want to do a little preparation before navigation
    override func prepare(for segue: UIStoryboardSegue, sender: Any?) {
        // Get the new view controller using segue.destinationViewController.
        // Pass the selected object to the new view controller.
        if segue.identifier == "ActivityDetailSegue" {
            let activityDetailViewController = segue.destination as! ActivityDetailViewController
            activityDetailViewController.activity = activities[tableView.indexPathForSelectedRow!.row]
        }
    }
    
    
    @IBAction func unwindToActivityTable (segue: UIStoryboardSegue) {
        tableView.reloadData()
    }

